use super::{GemmParams, KernelFn};
use core::arch::asm;

// AVX2 GEMM micro-kernels for bf16-packed B matrices.
//
// Same structure as avx2.rs but B loads use vpmovzxwd + vpslld to convert
// bf16 (u16) → f32 inline. B pointer advances 32 bytes per K step (vs 64 for f32).
//
// bf16→f32: vpmovzxwd ymm, xmmword ptr [ptr]  (8 × u16 → 8 × u32)
//           vpslld    ymm, ymm, 16             (shift left = bf16 mantissa alignment)

#[inline(never)]
unsafe fn avx2_bf16_kernel_1x2(gp: *mut GemmParams) {
    asm!(
        "push rbx",
        "mov r14, {gp}",
        "mov r8, [r14 + 0]",    // k
        "dec r8",
        "mov r9, [r14 + 8]",    // A
        "mov r10, [r14 + 16]",  // B (bf16 packed)
        "lea r15, [r14 + 24]",  // &beta
        "mov r12, [r14 + 32]",  // C
        "mov r13, [r14 + 40]",  // ldc
        "mov rdi, [r14 + 48]",  // b_block_cols
        "mov rsi, [r14 + 56]",
        "mov rax, r9",
        "mov rcx, r12",
        "xor ebx, ebx",
        "20:",                   // loop_outter
        "mov r14, r8",
        "vbroadcastss ymm15, dword ptr [r15]",
        // Load B[0]: bf16→f32
        "vpmovzxwd ymm3, xmmword ptr [r10 + 0]",
        "vpslld ymm3, ymm3, 16",
        "vpmovzxwd ymm4, xmmword ptr [r10 + 16]",
        "vpslld ymm4, ymm4, 16",
        "vxorps xmm0, xmm0, xmm0",
        "vcomiss xmm15, xmm0",
        "jz 21f",
        // beta != 0
        "vmulps ymm0, ymm15, ymmword ptr [r12 + 0]",
        "vmulps ymm1, ymm15, ymmword ptr [r12 + 32]",
        "test r14, r14",
        "jz 22f",
        // Prefetch B[1]: bf16→f32
        "vpmovzxwd ymm15, xmmword ptr [r10 + 32]",
        "vpslld ymm15, ymm15, 16",
        "22:",
        "vbroadcastss ymm2, dword ptr [r9+0]",
        "vfmadd231ps ymm0, ymm3, ymm2",
        "vfmadd231ps ymm1, ymm4, ymm2",
        "test r14, r14",
        "jnz 25f",
        "add r10, 32",
        "jmp 26f",
        "21:",                   // zero_regs
        "test r14, r14",
        "jz 23f",
        "vpmovzxwd ymm15, xmmword ptr [r10 + 32]",
        "vpslld ymm15, ymm15, 16",
        "23:",
        "vbroadcastss ymm2, dword ptr [r9+0]",
        "vmulps ymm0, ymm3, ymm2",
        "vmulps ymm1, ymm4, ymm2",
        "test r14, r14",
        "jnz 25f",
        "add r10, 32",
        "jmp 26f",
        "24:",                   // loop_inner
        "vmovaps ymm3, ymm15",
        "vpmovzxwd ymm4, xmmword ptr [r10 + 16]",
        "vpslld ymm4, ymm4, 16",
        "vpmovzxwd ymm15, xmmword ptr [r10 + 32]",
        "vpslld ymm15, ymm15, 16",
        "vbroadcastss ymm2, dword ptr [r9+0]",
        "vfmadd231ps ymm0, ymm3, ymm2",
        "vfmadd231ps ymm1, ymm4, ymm2",
        "25:",                   // next_inner
        "add r9, 4",
        "add r10, 32",
        "dec r14",
        "jnz 24b",
        "vmovaps ymm3, ymm15",
        "vpmovzxwd ymm4, xmmword ptr [r10 + 16]",
        "vpslld ymm4, ymm4, 16",
        "vbroadcastss ymm2, dword ptr [r9+0]",
        "vfmadd231ps ymm0, ymm3, ymm2",
        "vfmadd231ps ymm1, ymm4, ymm2",
        "add r9, 4",
        "add r10, 32",
        "26:",                   // dump_C
        "vmovups ymmword ptr [r12 + 0], ymm0",
        "vmovups ymmword ptr [r12 + 32], ymm1",
        "add rcx, 64",
        "mov r12, rcx",
        "mov r9, rax",
        "inc rbx",
        "cmp rbx, rdi",
        "jl 20b",
        "pop rbx",
        gp = in(reg) gp,
        out("rax") _, out("rcx") _,
        out("rdi") _, out("rsi") _,
        out("r8") _, out("r9") _, out("r10") _,
        out("r12") _, out("r13") _, out("r14") _, out("r15") _,
        out("ymm0") _, out("ymm1") _, out("ymm2") _, out("ymm3") _,
        out("ymm4") _, out("ymm15") _,
    );
}

