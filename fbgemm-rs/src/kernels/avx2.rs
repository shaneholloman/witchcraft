use super::{GemmParams, KernelFn};
use core::arch::asm;

// AVX2 GEMM micro-kernels ported from FBGEMM's FbgemmFP32UKernelsAvx2.cc
// GemmParams field offsets: k=0, A=8, B=16, beta=24, C=32, ldc=40, b_block_cols=48, b_block_size=56
// Note: on x86_64, A is pre-transposed (column-major via PackA), so A stride = kernel_nrows * 4 bytes

#[inline(never)]
unsafe fn avx2_kernel_1x2(gp: *mut GemmParams) {
    asm!(
        "push rbx",
        "mov r14, {gp}",
        "mov r8, [r14 + 0]",    // k
        "dec r8",
        "mov r9, [r14 + 8]",    // A
        "mov r10, [r14 + 16]",  // B
        "lea r15, [r14 + 24]",  // &beta
        "mov r12, [r14 + 32]",  // C
        "mov r13, [r14 + 40]",  // ldc
        "mov rdi, [r14 + 48]",  // b_block_cols
        "mov rsi, [r14 + 56]",  // b_block_size
        "mov rax, r9",
        "mov rcx, r12",
        "xor ebx, ebx",
        "20:",                   // loop_outter
        "mov r14, r8",
        "vbroadcastss ymm15, dword ptr [r15]",
        "vmovups ymm3, ymmword ptr [r10 + 0]",
        "vmovups ymm4, ymmword ptr [r10 + 32]",
        "vxorps xmm0, xmm0, xmm0",
        "vcomiss xmm15, xmm0",
        "jz 21f",
        // beta != 0
        "vmulps ymm0, ymm15, ymmword ptr [r12 + 0]",
        "vmulps ymm1, ymm15, ymmword ptr [r12 + 32]",
        "test r14, r14",
        "jz 22f",
        "vmovups ymm15, ymmword ptr [r10 + 64]",
        "22:",
        "vbroadcastss ymm2, dword ptr [r9+0]",
        "vfmadd231ps ymm0, ymm3, ymm2",
        "vfmadd231ps ymm1, ymm4, ymm2",
        "test r14, r14",
        "jnz 25f",
        "add r10, 64",
        "jmp 26f",
        "21:",                   // zero_regs
        "test r14, r14",
        "jz 23f",
        "vmovups ymm15, ymmword ptr [r10 + 64]",
        "23:",
        "vbroadcastss ymm2, dword ptr [r9+0]",
        "vmulps ymm0, ymm3, ymm2",
        "vmulps ymm1, ymm4, ymm2",
        "test r14, r14",
        "jnz 25f",
        "add r10, 64",
        "jmp 26f",
        "24:",                   // loop_inner
        "vmovaps ymm3, ymm15",
        "vmovups ymm4, ymmword ptr [r10 + 32]",
        "vmovups ymm15, ymmword ptr [r10 + 64]",
        "vbroadcastss ymm2, dword ptr [r9+0]",
        "vfmadd231ps ymm0, ymm3, ymm2",
        "vfmadd231ps ymm1, ymm4, ymm2",
        "25:",                   // next_inner
        "add r9, 4",
        "add r10, 64",
        "dec r14",
        "jnz 24b",
        "vmovaps ymm3, ymm15",
        "vmovups ymm4, ymmword ptr [r10 + 32]",
        "vbroadcastss ymm2, dword ptr [r9+0]",
        "vfmadd231ps ymm0, ymm3, ymm2",
        "vfmadd231ps ymm1, ymm4, ymm2",
        "add r9, 4",
        "add r10, 64",
        "26:",                   // dump_C
        "vmovups ymmword ptr [r12 + 0], ymm0",
        "vmovups ymmword ptr [r12 + 32], ymm1",
        "add rcx, 64",
        "mov r12, rcx",
        "mov r9, rax",
        "inc rbx",
        "cmp rbx, rdi",
        "jl 20b",
        "pop rbx",
        gp = in(reg) gp,
        out("rax") _, out("rcx") _,
        out("rdi") _, out("rsi") _,
        out("r8") _, out("r9") _, out("r10") _,
        out("r12") _, out("r13") _, out("r14") _, out("r15") _,
        out("ymm0") _, out("ymm1") _, out("ymm2") _, out("ymm3") _,
        out("ymm4") _, out("ymm15") _,
    );
}

